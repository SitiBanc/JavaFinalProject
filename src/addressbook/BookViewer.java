    public void display(AddressBook ab){
        NameField.setText(ab.getName());
        MobileNoField.setText(ab.getNumber());
        BirthdayField.setText(ab.getBirthday());
        MailAdrField.setText(ab.getMailAddress());
        if(ab instanceof Relatives){
            Relatives r = (Relatives) ab;
            CategoryBox.setSelectedIndex(0);
            Attribute1Label.setText("關係");
            Attribute1Field.setText(r.getRelationship());
            Attribute2Label.setText("住家電話");
            Attribute2Field.setText(r.getPhoneNumber());
        }
        if(ab instanceof Colleague){
            CategoryBox.setSelectedIndex(1);
            Colleague c = (Colleague) ab;
            Attribute1Label.setText("部門");
            Attribute1Field.setText(c.getDepartment());
            Attribute2Label.setText("部門電話");
            Attribute2Field.setText(c.getDepartmentNumber());
        }
        if(ab instanceof Friend){
            CategoryBox.setSelectedIndex(2);
            Friend f = (Friend) ab;
            Attribute1Label.setText("暱稱");
            Attribute1Field.setText(f.getNickname());
            Attribute2Label.setText("住家地址");
            Attribute2Field.setText(f.getHouseAddress());
         }    
    }
    public void displayList(ArrayList list2, int type){
        AddressBook ab;
        DefaultListModel dlm = new DefaultListModel();
        String[] number = new String[list2.size()];
        for (Object list1 : list2) {
            ab = (AddressBook) list1;
           number[list2.indexOf(list1)] = ab.getNumber();
            switch(type){
                case 1:
                    if(ab instanceof Relatives)
                        dlm.addElement(ab.getNumber());
                    break;
                case 2:
                    if(ab instanceof Colleague)
                        dlm.addElement(ab.getNumber());
                    break;
                case 3:
                    if(ab instanceof Friend)
                        dlm.addElement(ab.getNumber());
                    break;
                default:
                    dlm.addElement(ab.getNumber());
            }
        }
        jList1.setModel(dlm);
    }
