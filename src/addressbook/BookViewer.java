    private void SaveFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveFileActionPerformed
       int ans =  JOptionPane.showConfirmDialog(null, "是否要儲存檔案？", "儲存檔案", JOptionPane.YES_NO_OPTION);
        if (ans == JOptionPane.YES_OPTION){
            try{
                String name = JOptionPane.showInputDialog(null,"請輸入檔名：","儲存檔案",JOptionPane.INFORMATION_MESSAGE);
                abr.save(name);
            }catch(IOException e){
                JOptionPane.showMessageDialog(null, "檔案開啟錯誤", "錯誤",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_SaveFileActionPerformed

    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
        setEditable(false);
        String n = "";
        try{
            n = JOptionPane.showInputDialog(null, "請輸入姓名：", "查詢", JOptionPane.QUESTION_MESSAGE);
            AddressBook ab = (AddressBook) abr.search(n);
            display(ab);
        }catch(NumberFormatException | NullPointerException e){
            JOptionPane.showMessageDialog(null, "找不到" + n + "！", "Oops！", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_SearchButtonActionPerformed

