    private void ConfirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmButtonActionPerformed
        int index = CategoryBox.getSelectedIndex();
        switch(index){
            case 0:
                Relatives r = new Relatives(NameField.getText(),MobileNoField.getText());
                r.setBirthday(BirthdayField.getText());
                r.setMailAddress(MailAdrField.getText());                
                r.setRelationship(Attribute1Field.getText());
                r.setPhoneNumber(Attribute2Field.getText());
                 abr.update(r);
                break;
            case 1:
                Colleague c = new Colleague(NameField.getText(),MobileNoField.getText());
                c.setBirthday(BirthdayField.getText());
                c.setMailAddress(MailAdrField.getText());
                c.setDepartment(Attribute1Field.getText());
                c.setDepartNumber(Attribute2Field.getText());
                abr.update(c);
                break;
            default:
                Friend f = new Friend(NameField.getText(),MobileNoField.getText());
                f.setBirthday(BirthdayField.getText());
                f.setMailAddress(MailAdrField.getText());
                f.setNickname(Attribute1Field.getText());
                f.setHouseAddress(Attribute2Field.getText());
                abr.update(f);
                break;
        }
        setEditable(false);
    }//GEN-LAST:event_ConfirmButtonActionPerformed

